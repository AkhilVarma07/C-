#include <iostream>

int main() {
    
    int integerNum = 10;
    std::cout << "Integer number: " << integerNum << std::endl;

    
    float floatNum = 3.14;
    std::cout << "Float number: " << floatNum << std::endl;

    
    char charValue = 'A';
    std::cout << "Character: " << charValue << std::endl;

    return 0;
}

